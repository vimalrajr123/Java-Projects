package com.dbs.bank.ibanking.model;

public class UserResponse {
	
	//DTO class
	private int code;
	private String desc;
	private Object user;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public Object getUser() {
		return user;
	}
	public void setUser(Object user) {
		this.user = user;
	}
	
	
	

}
